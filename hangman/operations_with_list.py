# Write a function that would take two lists and return one merged list,
# e.g. for the lists ['Washington, D.C.', 'Chicago', 'New York'] and
# ['Los Angeles', 'Las Vegas'] the result should be
# ['Washington, D.C.', 'Chicago', 'New York', 'Los Angeles', 'Las Vegas'].
# You can use any of the methods described in the theory.
# Don't print the merged list, just return it at the end of the function.
def merge_lists(list_one, list_two):
    return list_one + list_two
